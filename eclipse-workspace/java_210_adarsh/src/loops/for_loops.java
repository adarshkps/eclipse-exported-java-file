package loops;

public class for_loops {
	public static void main(String[] args) {
		
		for (int a=3;a<=5;a++)
		{
			System.out.println("hello world " +a);
			a++;
		}
		for (int i=12; i>5;--i)
		{
			System.out.println("hii adarsh "+i);
			--i;
		}
		for (int i=5;i<10;i++)
		{
			System.out.println("print java "+i);
		}
		for (int i=10;i>8;i--)
		{
			System.out.println("hiii");
		}
		for(int d=10;d>0;--d)
		{
			System.out.println("adarsh"); 
			d--;
		}
		for (int i=40; i>35;i--)
		{
			System.out.println("hello this world "+i);
			i--;
		}
		for (int j=10;j>=1;j--)
		{
			System.out.println(j);
		}
		for (int k=1;k<=10;k++)
		{
			System.out.println(k);
		}
		for (int x=1;x<4;x++)
		{
			System.out.println("hello hii good morning "+x);
			x++;
		}
		
	}

}
