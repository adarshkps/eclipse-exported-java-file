package com.nesting.loops;

public class nesting_for_loop {
	public static void main(String[] args) {
		
		for (int i=0;i<3;i++)
		{
			System.out.println("java "+i);
			for (int j=0;j<2;j++)
				{
				System.out.println("hello world "+j);
				}                                                                                                                                                                                 
			System.out.println("selenium "+i); 
		}
	}

}
