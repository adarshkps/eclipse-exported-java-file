package controlstructure_practice;

public class ladder_if {

	public static void main(String[] args) {
		
		int a=100;
		int b=10;
		if(a>b)
		{
			System.out.println(a+" is greater than "+b);
		}
		else if(a<b)
		{
			System.out.println("a is less than b");
		}
		else
		{
			System.out.println("a and b are equal");
		}
		
	}
	

}
